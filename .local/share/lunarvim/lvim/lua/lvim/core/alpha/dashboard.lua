local function getGreeting(name)
  local tableTime = os.date("*t")
  local hour = tableTime.hour
  local greetingsTable = {
    [0] = "󰢠  Early bird!",
    [1] = "  Good morning",
    [2] = "  Good afternoon",
    [3] = "  Good evening",
    [4] = "󰖔  Good night",
    [5] = "󰋣  Sleep well",
  }

  local greetingIndex = 0
  if hour < 6 then
    greetingIndex = 5
  elseif hour < 8 then
    greetingIndex = 0
  elseif hour < 12 then
    greetingIndex = 1
  elseif hour < 17 then
    greetingIndex = 2
  elseif hour < 20 then
    greetingIndex = 3
  elseif hour < 23 then
    greetingIndex = 4
  else
    greetingIndex = 5
  end

  return greetingsTable[greetingIndex]..", "..user_name
  end


